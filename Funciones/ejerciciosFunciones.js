// Área círculo.
function areaCirc(r)
{
    return Math.PI * (r * r)
}


// Volúmen esfera.
function volEsfera(r)
{
    return (4/3) * Math.PI * (r * r * r) 
}


// Máximo entre dos números a y b.
function max (a , b)
{
    return Math.max (a , b)
}


